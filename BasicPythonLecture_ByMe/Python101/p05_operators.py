# =============================================================================
# Python divides the operators in the following groups:
# 
# Arithmetic operators
# Assignment operators
# Comparison operators
# Logical operators
# Identity operators
# Membership operators
# Bitwise operators
# =============================================================================

# =============================================================================
# Arithmetic Operators
# =============================================================================
# Operator	  Name	           Example	
# +	          Addition	       x + y	
# -	          Subtraction	   x - y	
# *        	  Multiplication   x * y	
# /	          Division	       x / y	
# %	          Modulus	       x % y	
# **	      Exponentiation   x ** y	
# //	      Floor division   x // y
# =============================================================================


# =============================================================================
# Assignment Operators
# =============================================================================
# Operator  	Example  	Same As	
# =	            x = 5	    x = 5	
# +=	        x += 3	    x = x + 3	
# -=	        x -= 3	    x = x - 3	
# *=	        x *= 3	    x = x * 3	
# /=	        x /= 3   	x = x / 3	
# %=	        x %= 3	    x = x % 3	
# //=	        x //= 3	    x = x // 3	
# **=	        x **= 3	    x = x ** 3	
# &=	        x &= 3	    x = x & 3	
# |=	        x |= 3	    x = x | 3	
# ^=	        x ^= 3	    x = x ^ 3	
# >>=	        x >>= 3	    x = x >> 3	
# <<=	        x <<= 3	    x = x << 3	
# :=	    print(x := 3)	x = 3
#                           print(x)	
# 
# =============================================================================



# =============================================================================
# Comparison Operators
# =============================================================================
# Operator	Name	                    Example	
# ==	    Equal	                    x == y	
# !=	    Not equal	                x != y	
# >	        Greater than	            x > y	
# <	        Less than	                x < y	
# >=	    Greater than or equal to	x >= y	
# <=	    Less than or equal to	    x <= y
# =============================================================================



# =============================================================================
# Logical Operators
# =============================================================================
# Operator	Example	
# and 	    x < 5 and  x < 10	
# or	    x < 5 or x < 4	
# not	    not(x < 5 and x < 10)
# =============================================================================


# =============================================================================
# Identity Operators
# =============================================================================
# Operator	Example
# is 	        x is y	
# is not	    x is not y
# =============================================================================




# =============================================================================
# Membership Operators
# =============================================================================
# Operator	    Example
# in 	        x in y	
# not in 	    x not in y
# =============================================================================



# =============================================================================
# Bitwise Operators
# =============================================================================
# Operator	Name	            Example
# & 	    AND	                x & y	
# |	        OR	                x | y	
# ^	        XOR	                x ^ y	
# ~	        NOT	                ~x	
# <<	Zero fill left shift	x << 2	
# >>	Signed right shift	    x >> 2
# =============================================================================







